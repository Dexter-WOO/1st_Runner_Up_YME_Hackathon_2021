    private void LogButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogButActionPerformed

        String email = NameTxt.getText();
        String password = String.valueOf(PassTxt1.getPassword());
        tableMethod tm = new tableMethod();
        Boolean flag = false;
        ArrayList<String> allData =  tm.readTable("User");
        for(int i=0; i<allData.size(); i++){
            
            String id = allData.get(i).split("\t")[0];
            String user = allData.get(i).split("\t")[6];
            String pw = allData.get(i).split("\t")[8];
           
            if(user.equals(email) && password.equals(pw)){
               
                flag = true;
                break;
            }
        }
       
        if(flag==true){
            this.dispose();
            switch(Integer.parseInt(note.getText())){
                case 1: 
                    IndividualReceiver ind = new IndividualReceiver();
                    ind.setLocationRelativeTo(null);
                    ind.NewIndividualReceiver(email);
                    ind.setVisible(true);
                    
                    break;
                
                case 2:
                    FoodBankMenu fbm = new FoodBankMenu(email);
                    fbm.setLocationRelativeTo(null);
                    fbm.setVisible(true);
                    break;
                    
                case 3: 
                    IndividualReceiver HNGO = new IndividualReceiver();
                    HNGO.setLocationRelativeTo(null);
                    HNGO.NewIndividualReceiver(email);
                    HNGO.setInvisible();
                    HNGO.setVisible(true);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Invalid Username or Password.\nPlease type again.", "Error", JOptionPane.ERROR_MESSAGE);
            NameTxt.setText("");
            PassTxt1.setText("");
        }
    }//GEN-LAST:event_LogButActionPerformed

