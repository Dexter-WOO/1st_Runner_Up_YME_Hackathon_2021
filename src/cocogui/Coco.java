    private void registerFunc() 
    {
        tableMethod tm = new tableMethod();
        
        int count;
        String info[] = new String[9]; 
         
        info[0] = NameTxt1.getText(); 
        info[1] = AgeTxt.getText();
        info[2] = AddressTxt.getText();
        info[3] = String.valueOf(StateComboBox.getSelectedItem());
        info[4] = PNTxt1.getText();
        info[5] = EmailTxt1.getText();
        info[6] = note.getText();
        info[7] = PassTxt.getText();
        info[8] = ConfirmPassTxt.getText();
        
        if (info[1].equals("") || info[2].equals("") || info[3].equals("") || info[4].equals("") || info[5].equals("") || info[6].equals("")|| info[7].equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please Enter All Information Before Register", "WARNING", JOptionPane.ERROR_MESSAGE);
        }
        
        else if (info[8].equals(info[7])) //THE SAME STRING
        {
            String ss = info[0] + "\t" + info[1] + "\t" + info[2] + "\t" + info[3] + "\t" + info[4] + "\t" + info[5] + "\t" + info[6]+ "\t" + info[7];
          
            tm.appendData("User", ss);
            JOptionPane.showMessageDialog(this, "Registered Successfully");
            
            tm.foodbank(ss);
          
            Register.dispose();
        }
        else
        {
            ConfirmPassTxt.setText("");
            JOptionPane.showMessageDialog(this, "Invalid Password", "WARNING", JOptionPane.ERROR_MESSAGE);
        }
    }

